// JavaScript Document

$(function(){
	$("img.ChangePhoto").hover(function(){
		var ImgSrc = $(this).attr("src");
		var ImgAlt = $(this).attr("alt");
		$("img#MainPhoto").attr({src:ImgSrc,alt:ImgAlt});
		$("img#MainPhoto").fadeIn("fast");
		return false;
	});
});
	
